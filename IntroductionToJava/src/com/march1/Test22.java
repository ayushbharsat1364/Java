package com.march1;

public class Test22 {

	public static void main(String[] args) {
		int a=6, b=8;
		System.out.println("Before Swapping:");
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		int c=a;
		a=b;b=c;
		System.out.println("After Swapping:");
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		
		
		
		
		int y=60, z=80;
		System.out.println("Before Swapping:");
		System.out.println("a= "+y);
		System.out.println("b= "+z);
		y=y+z;
		z=y-z;
		y=y-z;
		System.out.println("After Swapping:");
		System.out.println("a= "+y);
		System.out.println("b= "+z);

	}

}
